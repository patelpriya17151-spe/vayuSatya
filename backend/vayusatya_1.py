def classify_source(row):
    so2  = row['mean_SO2']  if pd.notna(row['mean_SO2'])  else 0
    no2  = row['mean_NO2']  if pd.notna(row['mean_NO2'])  else 0
    co   = row['mean_CO']   if pd.notna(row['mean_CO'])   else 0
    pm25 = row['peak_PM25']

    # Industrial: high SO2 (>40 = above mean+SD), high PM2.5
    if so2 > 40 and pm25 > 80:
        return 'Industrial'
    # Traffic: elevated NO2 + CO, typical rush hour pattern
    if no2 > 55 and co > 1.0:
        return 'Traffic'
    # Dust/Biomass: very high PM2.5, low SO2 (combustion or resuspension)
    if pm25 > 100 and so2 < 20:
        return 'Dust/Biomass'
    return 'Mixed'

