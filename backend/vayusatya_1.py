import urllib.request

def fetch_historical_weather(lat=LAT, lon=LON, start='2024-01-01', end='2025-12-31'):
    """
    Open-Meteo provides free historical weather data — no API key required.
    We get daily wind speed and direction to correlate with surge events.
    """
    url = (
        f"https://archive-api.open-meteo.com/v1/archive?"
        f"latitude={lat}&longitude={lon}"
        f"&start_date={start}&end_date={end}"
        f"&daily=wind_speed_10m_max,wind_direction_10m_dominant,temperature_2m_max,temperature_2m_min"
        f"&wind_speed_unit=ms"
    )
    try:
        with urllib.request.urlopen(url, timeout=15) as response:
            return json.loads(response.read().decode())
    except Exception as e:
        print(f"Could not fetch: {e}")
        return None

