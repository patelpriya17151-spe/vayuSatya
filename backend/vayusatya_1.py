def fetch_weather(api_key, lat=LAT, lon=LON):
    """Fetch real-time weather for the sensor station location."""
    url = f"https://api.openweathermap.org/data/2.5/weather?lat={lat}&lon={lon}&appid={api_key}&units=metric"
    try:
        r = requests.get(url, timeout=10)
        data = r.json()
        if data.get('cod') != 200:
            print(f"❌ API Error: {data.get('message')}")
            return None
        return {
            'temperature_c': round(data['main']['temp'], 1),
            'humidity_pct': data['main']['humidity'],
            'pressure_hpa': data['main']['pressure'],
            'wind_speed_ms': round(data['wind']['speed'], 2),
            'wind_direction_deg': data['wind'].get('deg', 0),
            'weather_desc': data['weather'][0]['description'],
            'timestamp': data['dt'],
        }
    except Exception as e:
        print(f"❌ Network error: {e}")
        return None

def wind_direction_to_label(deg):
    """Convert degrees to compass direction."""
    dirs = ['N','NNE','NE','ENE','E','ESE','SE','SSE','S','SSW','SW','WSW','W','WNW','NW','NNW']
    return dirs[round(deg / 22.5) % 16]

def check_industrial_risk(wind_deg, wind_speed):
    """
    Cross-reference live wind with historical industrial surge patterns.
    Vatva GIDC is at ~135° from Maninagar.
    Wind FROM 135° means pollution blows FROM Vatva TOWARD sensor.
    """
    if wind_speed < 0.5:
        return "CALM", "Wind too calm for directional source inference. Monitor all pollutants."

    # Back-trajectory: pollution comes FROM upwind direction
    source_dir = (wind_deg + 180) % 360  # direction pollution comes from

    if 110 <= source_dir <= 160:
        return "HIGH", f"Pollution likely from SE direction ({source_dir:.0f}°) — VATVA GIDC zone. {63} Industrial surges in historical data match this pattern. Check SO2 levels."
    elif 290 <= source_dir <= 340:
        return "MODERATE", f"Pollution from NW ({source_dir:.0f}°) — Naroda industrial area. Monitor PM2.5."
    else:
        return "LOW", f"Wind source direction: {source_dir:.0f}° ({wind_direction_to_label(source_dir)}). No major industrial zone in upwind path."

